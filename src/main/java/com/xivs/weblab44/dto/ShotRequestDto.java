package com.xivs.weblab44.dto;

public class ShotRequestDto {
    private Float x;
    private Float y;
    private Float r;

    public Float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public Float getR() {
        return r;
    }

    public void setR(Float r) {
        this.r = r;
    }
}
